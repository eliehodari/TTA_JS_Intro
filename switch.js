let day = 'Monday';

switch(day){
    case 'Monday' :
         console.log('Today you work Onsite');
         break;

    case 'Tuesday' :
        console.log('Today you work Remotely');
        break;

    case 'Wednesday' :
         console.log('Today you have a work break');
         break;

    case 'Thursday' :
        console.log('Today you work Onsite');
        break;

    case 'Friday' :
        console.log('Today you work Remotely');
        break;

    default:
        console.log('Today is not work day');
        break;
        
}